	<div class='descrizionelegge'> La Repubblica tutela il lavoro in tutte le sue forme ed applicazioni. Cura la formazione e l'elevazione professionale dei lavoratori. Promuove e favorisce gli accordi e le organizzazioni internazionali intesi ad affermare e regolare i diritti del lavoro.</div>
</div>
<div>
	<div class='titololegge'>Art. 36 Costituzione</div>
	<div class='descrizionelegge'> Il lavoratore ha diritto ad una retribuzione proporzionata alla quantità e qualità del suo lavoro e in ogni caso sufficiente ad assicurare a sé e alla famiglia un'esistenza libera e dignitosa.</div>
