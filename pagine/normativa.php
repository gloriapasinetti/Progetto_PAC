	<div class='titololeggegrande'>Lo Statuto dei Lavoratori</div>
	<div class='descrizioneleggegrande'> Esso sancisce innanzitutto la libertà del lavoratore a non essere controllato nell’esecuzione del proprio lavoro.
		Lo Statuto dei Lavoratori regola poi il procedimento disciplinare, stabilendo precise regole che l’azienda deve seguire quanto intende sanzionare un lavoratore che non abbia rispettato i doveri di diligenza, obbedienza o fedeltà a pena di illegittimità della sanzione inflitta.
		Ancora, questa Legge rappresenta la maggiore fonte di tutela in caso di licenziamento illegittimo.</div>
</div>
<div>
	<div class='titololegge'>Tempo indeterminato</div>
	<div class='descrizionelegge'> Quello a tempo indeterminato è il tipico contratto di lavoro subordinato. Con questa forma contrattuale il lavoratore svolge, dietro pagamento di una retribuzione e senza limiti di durata, una prestazione di lavoro che viene diretta, organizzata e disciplinata dal datore di lavoro.</div>
</div>
<div>
	<div class='titololegge'>Tempo determinato</div>
	<div class='descrizionelegge'> Il contratto di lavoro a tempo determinato ha una durata già stabilita che non può essere superiore ai 36 mesi. Se, però, per la durata iniziale è stato fissato un periodo inferiore a 3 anni, il contratto può essere prorogato per una sola volta a patto che la durata complessiva (durata iniziale + proroga) non sia superiore a 3 anni.</div>

</div>
 <div>
	<div class='titololegge'>Tempo parziale (part-time)</div>
	<div class='descrizionelegge'> Il contratto di lavoro a tempo parziale è un contratto a termine o anche a tempo indeterminato che prevede un orario inferiore rispetto a quello previsto dalla legge.
Il part-time può essere di tre tipologie:
- orizzontale, se la riduzione riguarda l’orario giornaliero;
- verticale, se si lavora a tempo pieno per giorni limitati della settimana;
- misto, se vengono combinate le modalità del part-time orizzontale con quelle del verticale.</div>
</div>
